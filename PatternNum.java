//Left Triangle Pattern with every Row from 1 to number of Columns

public class PatternNum {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print(j);
			}
			System.out.print("\n");
		}

	}

}


